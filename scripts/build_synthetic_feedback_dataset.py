    rows = []
    for index, item in enumerate(COMMENTS, start=1):
        role, device, understandability, ui, disclaimer, install, trust, comment, tags, action = item
        rows.append(
            {
                "id": f"SPF-{index:03d}",
                "source_type": "synthetic_pre_pilot",
                "is_real_feedback": "false",
                "persona_group": role,
                "device": device,
                "understandability": understandability,
                "ui_clarity": ui,
                "disclaimer_clarity": disclaimer,
                "install_clarity": install,
                "trust": trust,
                "comment": comment,
                "issue_tags": tags,
                "action_taken": action,
            }
        )

    with OUT_CSV.open("w", encoding="utf-8-sig", newline="") as handle:
        writer = csv.DictWriter(handle, fieldnames=list(rows[0].keys()))
        writer.writeheader()
        writer.writerows(rows)

    tag_counter: Counter[str] = Counter()
    role_counter: Counter[str] = Counter()
